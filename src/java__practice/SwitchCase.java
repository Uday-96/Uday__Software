package java__practice;

public class SwitchCase {

public static void main(String[] args) {
		 int A=39;
		 
		switch(A) {
		
		case 1 :
		System.out.println(" I am 10");
		break;
		
		case 39 :
		System.out.println(" I am 10");
		break;
		
		
		}

	}

}
