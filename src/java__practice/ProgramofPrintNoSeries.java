// WAP to Print Numbers from 75 to 100
package java__practice;

public class ProgramofPrintNoSeries {

public static void main(String[] args) {
     for(int i=75; i>=25; i--)	{
    	 System.out.println(i);
     }	
// Alternative Method
     for(int i=25; i<=75; i++) {
    	 System.out.println(i); 
     }

	}

}
